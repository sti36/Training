"""Negatives, Zeros and Positives
На вход программе подаются натуральное число n, а затем n целых чисел. Напишите программу, которая сначала выводит все отрицательные числа, затем нули, а затем все положительные числа, каждое на отдельной строке. Числа должны быть выведены в том же порядке, в котором они были введены.

Формат входных данных
На вход программе подаются натуральное число n, а затем n целых чисел, каждое на отдельной строке.

Формат выходных данных
Программа должна вывести текст в соответствии с условием задачи."""

# Чтение первого числа N
n = int(input())

# Список для хранения чисел
numbers = []

# Считываем N чисел
for i in range(n):
    numbers.append(int(input()))

# Создаем списки для трех категорий
negatives = []     # отрицательные числа
zeros = []          # нули
positives = []      # положительные числа

# Распределяем числа по категориям
for num in numbers:
    if num < 0:
        negatives.append(num)
    elif num > 0:
        positives.append(num)
    else:
        zeros.append(num)

# Выводим результаты
print(*negatives, sep='\n')  # отрицательные числа
print(*zeros, sep='\n')       # нули
print(*positives, sep='\n')  # положительные числа