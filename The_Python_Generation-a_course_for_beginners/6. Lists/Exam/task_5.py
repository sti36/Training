"""Самый длинный ↔️
На вход программе подаётся строка текста. Напишите программу, использующую списочное выражение, которая находит длину самого длинного слова.

Формат входных данных
На вход программе подаётся строка текста.

Формат выходных данных
Программа должна вывести текст в соответствии с условием задачи."""

string = input()

max_len = max(len(word) for word in string.split())

print(max_len)