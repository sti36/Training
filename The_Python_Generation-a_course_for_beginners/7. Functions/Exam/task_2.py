# –æ–±—ä—è–≤–ª–µ–Ω–∏–µ —Ñ—É–Ω–∫—Ü–∏–∏
def get_shipping_cost(quantity):
    if quantity <= 0:
        return 0  # –ï—Å–ª–∏ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —Ç–æ–≤–∞—Ä–∞ –º–µ–Ω—å—à–µ –∏–ª–∏ —Ä–∞–≤–Ω–æ –Ω—É–ª—é, –¥–æ—Å—Ç–∞–≤–∫–∞ –±–µ—Å–ø–ª–∞—Ç–Ω–∞
    elif quantity == 1:
        return 1000  # –ü–µ—Ä–≤—ã–π —Ç–æ–≤–∞—Ä —Å—Ç–æ–∏—Ç 1000 —Ä—É–±–ª–µ–π
    else:
        return 1000 + (quantity - 1) * 120  # –î–æ–±–∞–≤–ª—è–µ–º 120 —Ä—É–±–ª–µ–π –∑–∞ –∫–∞–∂–¥—ã–π –¥–æ–ø–æ–ª–Ω–∏—Ç–µ–ª—å–Ω—ã–π —Ç–æ–≤–∞—Ä

# —Å—á–∏—Ç—ã–≤–∞–µ–º –¥–∞–Ω–Ω—ã–µ
n = int(input())

# –≤—ã–∑—ã–≤–∞–µ–º —Ñ—É–Ω–∫—Ü–∏—é
print(get_shipping_cost(n))