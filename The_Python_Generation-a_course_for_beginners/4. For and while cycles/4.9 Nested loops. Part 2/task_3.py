a, b = int(input()), int(input())

summ_count = 0
max_i = 0

for i in range(a, b + 1):
    total = 0
    for j in range(1, i + 1):
        if i % j == 0:
            total += j
        if total >= summ_count:
            summ_count = total
            max_i = i
            
print(max_i, summ_count)