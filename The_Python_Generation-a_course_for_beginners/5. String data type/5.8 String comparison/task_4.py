first_line = input()
second_line = input()

first_line_letters = ''
second_line_letters = ''

for s in first_line:
    if s.isalpha():
        first_line_letters += s.lower()

for s in second_line:
    if s.isalpha():
        second_line_letters += s.lower()

if first_line_letters == second_line_letters:
    print('YES')
else:
    print('NO')