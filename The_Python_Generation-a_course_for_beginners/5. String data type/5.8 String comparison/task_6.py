number = int(input())

# –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–µ –¥–ª—è —Ö—Ä–∞–Ω–µ–Ω–∏—è –ø—Ä–µ–¥—ã–¥—É—â–µ–π —Ñ–∞–º–∏–ª–∏–∏ –∞–≤—Ç–æ—Ä–∞ –∏ –Ω–∞–∑–≤–∞–Ω–∏—è –∫–Ω–∏–≥–∏
previous_book = ''
# –ø–µ—Ä–µ–º–µ–Ω–Ω–∞—è —Ñ–ª–∞–≥–∞ —É–∫–∞–∑—ã–≤–∞–µ—Ç, —Å–æ—Ö—Ä–∞–Ω–µ–Ω –ª–∏ –ø–æ—Ä—è–¥–æ–∫ —Å–æ—Ä—Ç–∏—Ä–æ–≤–∫–∏ –∫–Ω–∏–≥:
flag = 'YES'

